package triad.recyclerview;

public class ListModel {
    private String head;
    private  String subhead;

    public ListModel(String head, String subhead) {
        this.head = head;
        this.subhead = subhead;
    }

    public String getHead() {
        return head;
    }

    public String getSubhead() {
        return subhead;
    }


}
